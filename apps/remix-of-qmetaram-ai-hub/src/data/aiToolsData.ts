// Get trending tools sorted by engagement
export const getTrendingTools = (limit = 10) => {
  return aiToolsData
    .filter(tool => tool.isTrending)
    .sort((a, b) => (b.likesCount + b.commentsCount) - (a.likesCount + a.commentsCount))
    .slice(0, limit);
};

// Get tools by category
export const getToolsByCategory = (category: AIToolCategory) => {
  return aiToolsData.filter(tool => tool.category === category);
};

// Get featured tools
export const getFeaturedTools = () => {
  return aiToolsData.filter(tool => tool.isFeatured);
};

// Get category statistics
export const getCategoryStats = () => {
  const stats: Record<AIToolCategory, { count: number; avgLikes: number; avgComments: number; topTool: AITool }> = {} as any;
  
  Object.keys(categoryInfo).forEach(cat => {
    const tools = getToolsByCategory(cat as AIToolCategory);
    const avgLikes = tools.reduce((sum, t) => sum + t.likesCount, 0) / tools.length || 0;
    const avgComments = tools.reduce((sum, t) => sum + t.commentsCount, 0) / tools.length || 0;
    const topTool = tools.sort((a, b) => b.likesCount - a.likesCount)[0];
    
    stats[cat as AIToolCategory] = {
      count: tools.length,
      avgLikes: Math.round(avgLikes),
      avgComments: Math.round(avgComments),
      topTool
    };
  });
  
  return stats;
};
